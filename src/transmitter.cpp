uint8_t calcControllerChecksum(const ControllerData& c) {
    uint8_t cs = 0;
    const uint8_t* ptr = reinterpret_cast<const uint8_t*>(&c);
    for (size_t i = 0; i < offsetof(ControllerData, checksum); i++) cs ^= ptr[i];
    return cs;
}

void playBootAnimation() {
    for (int frame = 0; frame < ANIM_FRAME_COUNT; frame++) {
        for (uint8_t page = 0; page < 8; page++) {
            Wire.beginTransmission(OLED_I2C_ADDR);
            Wire.write(0x00);
            Wire.write(0xB0 + page);
            Wire.write(0x00);
            Wire.write(0x10);
            Wire.endTransmission();

            Wire.beginTransmission(OLED_I2C_ADDR);
            Wire.write(0x40);
            for (uint8_t col = 0; col < 128; col++) {
                uint8_t displayData = 0;
                if (col >= 40 && col < 88 && page >= 1 && page < 7) {
                    uint8_t anim_x      = col - 40;
                    uint8_t anim_y_page = page - 1;
                    for (uint8_t bit = 0; bit < 8; bit++) {
                        uint16_t pixel_y  = (anim_y_page * 8) + bit;
                        uint16_t byte_idx = (pixel_y * (ANIM_FRAME_WIDTH / 8)) + (anim_x / 8);
                        uint8_t  byte_val = pgm_read_byte(&(aircraft_frames[frame][byte_idx]));
                        if (byte_val & (0x80 >> (anim_x % 8))) displayData |= (1 << bit);
                    }
                }
                Wire.write(displayData);
            }
            Wire.endTransmission();
        }
        delay(ANIM_FRAME_DELAY);
    }
}

bool init_funk_tx() {
    SPI.setMOSI(NRF24_SPI_MOSI);
    SPI.setMISO(NRF24_SPI_MISO);
    SPI.setSCLK(NRF24_SPI_SCK);
    SPI.begin();
    if (!radio.begin(&SPI) || !radio.isChipConnected()) return false;
    radio.setChannel(NRF24_CHANNEL);
    radio.setPALevel(NRF24_PA_LEVEL);
    radio.setDataRate(NRF24_DATA_RATE);
    radio.setCRCLength(RF24_CRC_16);
    radio.setAddressWidth(5);
    radio.setAutoAck(true);
    radio.setRetries(1, 5);
    radio.enableDynamicPayloads();
    radio.enableAckPayload();
    radio.openWritingPipe(CONTROLLER_ADDRESS);
    return true;
}

